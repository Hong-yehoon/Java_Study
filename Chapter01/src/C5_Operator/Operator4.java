package C5_Operator;

public class Operator4 {

	public static void main(String[] args) {
		
		int n1=10;
		int n2=5;
		boolean result;
		
		result = n1 > n2 ;
		System.out.println(result);
		
		result = n1 < n2 ;
		System.out.println(result);
		
		result = n1 >= n2 ;
		System.out.println(result);
		
		result = n1 <= n2 ;
		System.out.println(result);
		
		result = n1 == n2 ; //같은지
		System.out.println(result);
		
		result = n1 != n2 ; 	//다른지
		System.out.println(result);
		

	
	
	}
}
