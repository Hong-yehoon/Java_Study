package C4_pakage;

public class D3_Int {

	public static void main(String[] args) {
		
		int var1 = 10;					//10진수
		int var2 = 0b1111;				//2진수
		int var3 = 013;					//8진수
		int var4 = 0xF;						//16진수

	 System.out.println("십진수 10은"+var1);
	 System.out.println("이진수 1111은"+var2);
	 System.out.println("8진수 13은"+var3);		//001 011 --->001011
	 System.out.println("16진수 F는"+var4);
	
	}
}
