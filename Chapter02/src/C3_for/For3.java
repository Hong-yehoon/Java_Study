package C3_for;

public class For3 {
	public static void main(String[] args) {
		
		for(int i=0; i<=10; i++) {
			System.out.print("*");
		}
		System.out.println("");
		for(int i=10; i>=0; i--) {
			System.out.print("*");
		}
		System.out.println();
		
		for(int i=0; i<=10; i+=2) {
			System.out.println("*");
		}
		//증감조건 두기 이상
		
		
		
	}
}
